def merge(intervals):
    """Merge overlapping and touching intervals.

    Given a list of ``(start, end)`` integer intervals (possibly unsorted),
    return a sorted list of merged, non-overlapping intervals. Two intervals
    that merely touch — e.g. ``(1, 2)`` and ``(2, 3)`` — must merge into a
    single interval ``(1, 3)``.

    STUB: not implemented yet. Returns the input unchanged, which is wrong for
    any case that requires merging or sorting.
    """
    return list(intervals)
